package com.a267;

public class MoEx1 
{
	
	static int add(int a,int b)
	{
		return a+b;
	}
	
	static int add(int a,int b,int c)
	{
		return a*b*c;
	}
	
	public static void main(String[] args) 
	{
		
		System.out.println(add(6,5));//addition
		System.out.println(add(2,3,4));//Multiplication
		
	}
	
}
