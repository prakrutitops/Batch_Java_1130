package com.a267;

public class MoEx2 
{
	
	static int add(int a,int b)
	{
		return a+b;
	}
	
	static double add(double a,double b)
	{
		return a*b;
	}
	
	public static void main(String[] args) 
	{
		
		System.out.println(add(6,5));
		System.out.println(add(2.0,3.0));
		
	}
	
}
